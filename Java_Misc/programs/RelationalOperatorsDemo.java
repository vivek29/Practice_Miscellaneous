package com.practice.vivek;

public class RelationalOperatorsDemo { 
	   public RelationalOperatorsDemo( ) { 

	     int x = 10, y = 5; 
	     System.out.println("x > y : "+(x > y)); 
	     System.out.println("x < y : "+(x < y)); 
	     System.out.println("x >= y : "+(x >= y)); 
	     System.out.println("x <= y : "+(x <= y)); 
	     System.out.println("x == y : "+(x == y));
	     System.out.println("x != y : "+(x != y)); 
	     } 

	     public static void main(String args[]){ 
	             new RelationalOperatorsDemo();
	     } 
	 }
